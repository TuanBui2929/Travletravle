package com.example.travletravle.nameclass;

public class Khachsan {
    int image;
    String ten;
    String diachi;
    String sdt;
    String gia;

    public Khachsan(int image, String ten, String diachi, String sdt, String gia) {
        this.image = image;
        this.ten = ten;
        this.diachi = diachi;
        this.sdt = sdt;
        this.gia = gia;
    }
    public int getImage() {
        return image;
    }
    public void setImage(int image) {
        this.image = image;
    }
    public String getTen() {
        return ten;
    }
    public void setTen(String ten) {
        this.ten = ten;
    }
    public String getDiachi() {
        return diachi;
    }
    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
    public String getSdt() {
        return sdt;
    }
    public void setSdt(String sdt) {
        this.sdt = sdt;
    }
    public String getGia() {
        return gia;
    }
    public void setGia(String gia) {
        this.gia = gia;
    }

}
